## Illustrate Boolean values.
if 7:
    print("A nonzero number is true.")
else:
    print("The number zero is false.")

if []:
    print("A nonempty list is true.")
else:
    print("An empty list is false.")

if ["spam"]:
    print("A nonempty list is true.")
else:
    print("The empty list is false.")    


